package com.example0203.demo0203.Practic3;

public class UnaryOp {
    public static void main(String[] args) {
        int d = +100;
        int e = -10;

        d++;
        System.out.println(d); // Output: 101

        d--;
        System.out.println(d); // Output: 100

        System.out.println(e); // Output: -10
    }
}
